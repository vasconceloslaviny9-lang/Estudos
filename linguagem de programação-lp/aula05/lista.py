numero=[2, 5, 10, 1, 4, 9, 22, 100, 3]
print(len(numero)) #Tamanho da lista
print(min(numero)) #Menor valor dfa lista
print(max(numero)) #Maximo valor da lista
print(sum(numero)) #Soma de todos os valores de uma vez
print(sorted(numero)) #Organiza do menor para o maior (crescente)
print(sorted(numero, reverse=True)) #Organiza do maior para o menor (descrecente)
print(numero[5]) #possição do indice
print(100 in numero) #se tem o numero na lista V=true F=false
print(23 in numero)
print(numero) 
print(numero[2:8])
print(numero[:2])
print(numero[2:])
del numero[1]
print(numero)
numero.append(101) #acrescenta o valor na lista (no final)
print(numero)
